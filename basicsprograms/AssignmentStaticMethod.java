package basicsprograms;

public class AssignmentStaticMethod {
	static void method1() {
		System.out.println("1st static method");
	}
	
	static void method2() {
		System.out.println("2nd static method");
	}
	
	static void method3() {
		System.out.println("3rd static method");
	}
	
	static void method4() {
		System.out.println("4th static method");
	}
	
	static void method5() {
		System.out.println("5th static method");
	}

	public static void main(String[] args) {
		
		method1();
		method2();
		method3();
		method4();
		method5();
		System.out.println("All are static methods");
	}

}
//call 5 static method inside the main method-Assignment 1
