package assignments;

public class WhileLoop1to100 {

	public static void main(String[] args) {
		
	//Initialization & declaration
		int num = 1; 

        // Using while loop to print numbers from 1 to 100
        while (num <= 100) {
            System.out.print(num + " ");
            num++; // Increment the counter
	}

}
}

